u32 Change_Datatype_645_to_698(u32 Datatype,u8 *psour,u8 *pdest)//根据数据类型 Datatype 把原始数据psour转成目标数据pdest,返回转换成功后的数据长度，失败返回0
{
	switch(Datatype)
	{
		case DataType_date_time_s:
			return Get_Datatype_date_time_s(psour,pdest);//把YYMMDDhhmmss转成698格式date_time_s  返回长度
		case DataType_date_time_5byte:
			return Get_Datatype_date_time_5type(psour,pdest);//把YYMMDDhhmm 格式的转date_time_s格式，返回长度
		case DataType_double_long:
			return Get_Datatype_double_long(psour,pdest);
		case DataType_double_long_unsigned:
			return Get_Datatype_double_long_unsigned(psour,pdest);
		case DataType_double_long_unsigned_3byte:
			return Get_Datatype_double_long_unsigned_3byte(psour,pdest);
		case DataType_long:
			return Get_Datatype_long(psour,pdest);
		case DataType_long_unsigned:
			return Get_Datatype_long_unsigned(psour,pdest);
		case DataType_Unbalance:
			return Get_Datatype_Unbalance(psour,pdest);
		case DataType_NULL:
			return Get_Datatype_null(psour,pdest);
		case DataType_date_time_s_null:
			return Get_Datatype_date_time_s_null(psour,pdest);
		case DataType_bit_string:
			return Get_Datatype_bit_string(psour,pdest);
		case DataType_double_long_unsigned_2byte:
			return Get_Datatype_double_long_unsigned_2byte(psour,pdest);
		case DataType_unsigned:
			return Get_Datatype_unsigned(psour,pdest);
		default:
			return 0;
	}
}

