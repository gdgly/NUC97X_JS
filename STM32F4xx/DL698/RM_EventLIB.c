u32 Get_Datatype_date_time_s(u8* psour,u8* pdest)//把YYMMDDhhmmss转成698格式date_time_s  返回长度
{
	u32 index=0;
	if((psour==0)&&(pdest==0))
		return 8;
	pdest[index++]=DataType_date_time_s;
	pdest[index++]=(bcd_hex(psour[5])+2000)>>8;
	pdest[index++]=(bcd_hex(psour[5])+2000)&0xff;
	pdest[index++]=bcd_hex(psour[4]);
	pdest[index++]=bcd_hex(psour[3]);
	pdest[index++]=bcd_hex(psour[2]);
	pdest[index++]=bcd_hex(psour[1]);
	pdest[index++]=bcd_hex(psour[0]);
	return index;
}
