void AFN0C_Configure(void)//终端支持的1类数据配置,出口:数据在ADDR_DATABUFF
{
//ADDR_DATABUFF定义如下:
//第0Byte 支持的用户大类号标志		BS16	2
//大类号0
//第2Byte 支持的信息类组数n(0～31)	BIN	1
//第3Byte F1-F8
//第4Byte F9-F16
//第5Byte F17-F24
//...
//大类号1-15同大类号0在此不填入
	u32 i;
	u32 x;
	u32 y;
	u32 MaxFn;
	u8 *p8;
	
	p8=(u8*)(ADDR_DATABUFF);
	p8[0]=0xff;//支持的用户大类号标志		BS16	2
	p8[1]=0xff;
	p8[2]=0;//支持的信息类组数n(0～31)=0
	x=0;
	y=0;
	MaxFn=Get_Class1Data_MaxFn();
	for(i=1;i<MaxFn;i++)
	{
		if(Terminal_Class1DataLib[i].project&(1<<(Project/100)))
		{
			if(Terminal_Class1DataLib[i].PnType==p0Type)//p0Type=终端信息点,pnType=测量点号,GnType=总加组号,CnType=电容器测量点号,DnType=直流模拟量号,RnType=控制轮次号,TnType=任务号,EnType=事件ERC号
			{
				y|=(1<<x);
			}
			else
			{
				if(Terminal_Class1DataLib[i].PnMax!=0)
				{
					y|=(1<<x);
				}
			}
		}
		x++;
		if(x>=8)
		{
			p8[p8[2]+3]=y;
			p8[2]++;
			x=0;
			y=0;
		}
	}
	if(y!=0)
	{
		p8[p8[2]+3]=y;
		p8[2]++;
	}
	//以下数据可由各分项组合得到总是有的
	//F33测量点正向有/无功电能示值、一/四象限无功电能示值（总、费率1～M，1≤M≤12）
	//F34测量点反向有/无功电能示值、二/三象限无功电能示值（总、费率1～M，1≤M≤12）
	//F35测量点正向有/无功最大需量及发生时间（总、费率1～M，1≤M≤12）
	//F36测量点反向有/无功最大需量及发生时间（总、费率1～M，1≤M≤12）
	//F37上月（上一结算日）测量点正向有/无功电能示值、一/四象限无功电能示值（总、费率1～M，1≤M≤12）
	//F38上月（上一结算日）测量点反向有/无功电能示值、二/三象限无功电能示值（总、费率1～M，1≤M≤12
	//F39上月（上一结算日）测量点正向有/无功最大需量及发生时间（总、费率1～M，1≤M≤12）
	//F40上月（上一结算日）测量点反向有/无功最大需量及发生时间（总、费率1～M，1≤M≤12
	p8[7]|=0xff;
}


